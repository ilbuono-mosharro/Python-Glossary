#Sequence Types: list, tuple, range

varible_list = ['Marinario', 'Mustafa', 31]
print(varible_list)
print(type(varible_list)) #Will print "list"

variable_tuple = ("Marinario", "Mustafa", 31)
print(variable_tuple)
print(type(variable_tuple)) #Will print "tuple"

variable_range = range(0, 10)
print(variable_range)
print(type(variable_range)) #Will print "range"