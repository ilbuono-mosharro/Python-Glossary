#Numeric Types:	int, float, complex

variable_integer = 15
print(variable_integer)
print(type(variable_integer)) #Will print "int"

variable_float = 20.356
print(variable_float)
print(type(variable_float)) #Will print "float"

variable_complex = 1j
print(variable_complex)
print(type(variable_complex)) #Will print "complex"